package ch08.class03;

public class Arith {

	public static int add(int num0, int num1) {
		return num0 + num1;
	}

	public static int sub(int num0, int num1) {
		return num0 - num1;
	}

	public static int mul(int num0, int num1) {
		return num0 * num1;
	}

	public static int div(int num0, int num1) {
		return num0 / num1;
	}
}
